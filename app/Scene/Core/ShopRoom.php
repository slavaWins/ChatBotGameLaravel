     * @param User $user –¥–ª—è –∏–≥—Ä–æ–∫–∞
     * @param Class_ $characterClass —Ç–æ–≤–∞—Ä –±—É–¥–µ—Ç —ç—Ç–æ–≥–æ –∫–ª–∞—Å—Å–∞, Character
     * @param Class_ $itemShopClass —ç—Ç–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ –¥–ª—è —Ç–æ–≤–∞—Ä–∞, –æ–Ω–æ —Å–æ–¥–µ—Ä–∂–∏—Ç –∏–Ω—Ñ—É –æ —Ç–æ–≤–∞—Ä–µ. ItemCharacterShop
     * @param int $forParentId –ø–æ—Å–ª–µ –ø–æ–∫—É–ø–∫–∏ —á–∞—Ä–∞–∫—Ç–µ—Ä–∞, –≤–ª–∞–¥–µ–ª—å—Ü–µ–º –±—É–¥–µ—Ç —Ç–∞–∫ –∂–µ —á–∏—Å–ª–∏—Ç—Å—è —ç—Ç–æ—Ç –ß–∞—Ä–∞–∫—Ç–µ—Ä –∫–∞–∫ —Ä–æ–¥–∏—Ç–µ–ª—å
     * @return ShopRoom
     */
    public static function CreateShopRoomByCharacterType(User $user, $itemShopClass, $forParentId = 0): ShopRoom
    {

        $request = new BotRequestStructure();
        $request->user = $user;
        $skillRoom = new ShopRoom($request);


        $characterClass = $itemShopClass::characterClass;

        $skillRoom->scene->sceneData = [
            'page' => 1,
            'characterClass' => $characterClass,
            'itemShopClass' => $itemShopClass,
            'selectId' => 0,
            'forParentId' => $forParentId,
            'showMyPrice' => true, //–ø–æ–∫–∞–∑—ã–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –¥–æ—Å—É—Ç–ø–Ω—ã–µ –º–Ω–µ –ø–æ —Ü–µ–Ω–µ
            'filter_val' => "", //–ø–æ –∑–Ω–∞—á–µ–Ω–∏—é –ø–æ–ª—è makra = Nissan
        ];

        $skillRoom->scene->save();
        $skillRoom->scene->refresh();
        return $skillRoom;
    }


