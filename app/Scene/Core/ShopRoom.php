        $this->response->message .= "\n\n –¢–æ–ª—å–∫–æ –¥–æ—Å—Ç—É–ø–Ω—ã–µ —Ç–æ–≤–∞—Ä—ã: ";
        if ($this->scene->sceneData['showMyPrice']) {
            $this->response->message .= "–î–∞ (" . $items->count() . ")";
        } else {
            $this->response->message .= "–í—Å–µ —Ç–æ–≤–∞—Ä—ã (" . $items->count() . ")";
